	<h1>Hi-Lo: <span>The Rules</span></h1>
	
	<p><b>The goal is to end the game with as few points as possible.</b></p>
	<h2>How to play:</h2>
	<p>Play consists of a dealer and a player.</p>
	<ol>
		<li>The dealer draws a card from the top of the deck and places it face up.</li>
		<li>The player must guess whether the next card drawn from the deck will be higher or lower than the face up card.</li>
		<li>Once the player guesses, the dealer draws the next card and places it face up on top of the previous card.</li>
		<li>If the player is correct, go back to step 2.</li>
		<li>If the player is wrong, the player receives a point for each card in the face up pile, and the face up pile is discarded. Then play begins at step 1 again.</li>
	</ol>
	<p>When the player has made three correct guesses in a row, they may make another guess, or choose to pass and the roles are reversed with the face up pile continuing to build. The player may choose to continue if there is a high likelihood that their next guess would be correct. If they are wrong, play starts over at step 1 and the player must again make three correct guesses before being allowed to pass. If they are correct, they can continue or pass.</p>
	<hr />
	<h2>Scoring:</h2>
	<ul>
		<li>Aces are high.</li>
		<li>The game ranks suits in ascending alphabetical order. Ie, spades > hearts > diamonds > clubs.</li>
		<li>In the event of a tie, player 2 wins. So don't tie, player 1.</li>
		<li>If the game ends with points on the line, those points are discarded.</li>
	</ul>
	<hr />
	<h2>Protips:</h2>
	<p>Use your keyboard to make moves when it's your turn:</p>
	<ul>
		<li><b>Enter: </b> Draw a card.</li>
		<li><b>Up arrow: </b> Guess high.</li>
		<li><b>Down arrow: </b> Guess low.</li>
		<li><b>Right arrow: </b> Pass.</li>
	</ul>
	<p>Click your name to change it anytime during the game.</p>
	<p>It's also worth mentioning that there's a cheat mode. If you can <a href="https://github.com/tublitzed/hi-lo">find it</a>, you can use it. It'll allow you to draw more cards at a time.</p>
